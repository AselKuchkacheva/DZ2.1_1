package com.company;

public class Condo {
    private String nameCondo;
    private String address;

    public Condo(String nameCondo, String address) {
        this.nameCondo = nameCondo;
        this.address = address;
    }

    public String getNameCondo() {
        return nameCondo;
    }

    public String getAddress() {
        return address;
    }
}
